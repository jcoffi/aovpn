<#

.SYNOPSIS
    PowerShell script to configured SSTP and IKEv2 VPN ports on a Windows Server Routing and Remote Access Service (RRAS) server.

.PARAMETER SstpPorts
    Specifieds the number of SSTP ports to provsion on the RRAS VPN server.

.PARAMETER Ikev2Ports
    Specifieds the number of IKEv2 ports to provsion on the RRAS VPN server.

.EXAMPLE
    .\Set-VpnPortConfiguration.ps1 -SstpPorts 2000 -Ikev2Ports 2000

    Running this PowerShell command will configure 2000 SSTP and IKEv2 ports.

.DESCRIPTION
    This script is used to quickly configure SSTP and IKEv2 VPN ports. It also disables the GRE, PPPOE, PPTP, and L2TP legacy VPN protocols.

.LINK
    https://directaccess.richardhicks.com/

.NOTES
    Version:        1.1
    Creation Date:  February 13, 2020
    Last Updated:   May 23, 2020
    Author:         Richard Hicks
    Organization:   Richard M. Hicks Consulting, Inc.
    Contact:        rich@richardhicks.com
    Web Site:       https://www.richardhicks.com/

#>

[CmdletBinding()]

Param(

    [Parameter(Mandatory = $True, HelpMessage = "Enter the number of IKEv2 ports to enable.")]
    [string]$Ikev2Ports = '128',
    [Parameter(Mandatory = $True, HelpMessage = "Enter the number of SSTP ports to enable.")]
    [string]$SstpPorts = '128'

)

Write-Verbose 'Disabling PPPOE ports...'
Invoke-Command -ScriptBlock { netsh ras set wanports device = "WAN Miniport (PPPOE)" ddoutonly = disabled maxports = 1 }

Write-Verbose 'Disabling GRE ports...'
Invoke-Command -ScriptBlock { netsh ras set wanports device = "WAN Miniport (GRE)" rasinonly = disabled ddinout = disabled ddoutonly = disabled maxports = 0 }

Write-Verbose 'Disabling L2TP ports...'
Invoke-Command -ScriptBlock { netsh ras set wanports device = "WAN Miniport (L2TP)" rasinonly = disabled ddinout = disabled ddoutonly = disabled maxports = 0 }

Write-Verbose 'Disabling PPTP ports...'
Invoke-Command -ScriptBlock { netsh ras set wanports device = "WAN Miniport (PPTP)" rasinonly = disabled ddinout = disabled ddoutonly = disabled maxports = 1 }

Write-Verbose "Configuring $SstpPorts SSTP ports..."
Invoke-Command -ScriptBlock { netsh ras set wanports device = "WAN Miniport (SSTP)" rasinonly = enabled ddinout = disabled ddoutonly = disabled maxports = $SstpPorts }

Write-Verbose "Configuring $Ikev2Ports IKEv2 ports..."
Invoke-Command -ScriptBlock { netsh ras set wanports device = "WAN Miniport (IKEv2)" rasinonly = enabled ddinout = disabled ddoutonly = disabled maxports = $Ikev2Ports }

Write-Warning 'The server must be restarted for these changes to take effect.'

